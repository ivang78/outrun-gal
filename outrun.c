/*
	draw background
*/
void draw_bg () {
	for (char i = 0; i < 96; i++) {
		z80_bpoke(SCREEN_ADDR + 96 + i, bg[i]);
	}
}

void draw_road () {
	char yp0 = 15;
	char rs, rh;
	int addr0 = SCREEN_ADDR;
	rh = road_half;
	for (char j = 0; j < 10; j++) {		
		if (j%2 == road_pos%2) {
			rs = 204;
		} else {
			rs = 243;
		}
		for (char i = 0; i < road_center - rh; i++) {
			z80_bpoke(addr0 + ((yp0 - j) << 5) + i, rs);
		}
		for (char i = road_center + rh + 2; i < 32; i++) {
			z80_bpoke(addr0 + ((yp0 - j) << 5) + i, rs);
		}
		rh--;
	}
	road_pos++;
}

/*
	draw sprite
	xp: x position
*/
void draw_sprite (char xp, char yp) {
	int addr0 = SCREEN_ADDR + (yp << 5) + xp;
	for (char j = 0; j < 3; j++) {
		for (char i = 0; i < 10; i++) {
			z80_bpoke(addr0 + (j << 5) + i, sprite[(j << 5) + i]);
		}
	}
}


/*
	Main function
*/
int main() {	
	rnd = z80_wpeek(RND_ADDR);
	srand(rnd);
	gal_cls();
	do {
		gal_cls();
		draw_bg ();
		x_pos = 11;
		y_pos = 12;
		road_half = 15;
		road_center = 15;
		road_pos = 0;
		draw_sprite(x_pos, y_pos);
		do {		
			draw_road();
			c = getk();
			switch (c) {
				case 45: // left 
					if (x_pos > 0) {
						x_pos--;
					}
					draw_sprite(x_pos, y_pos);
					break;
				case 46: // right
					if (x_pos < 32) {
						x_pos++;
					}
					draw_sprite(x_pos, y_pos);
					break;
				case 43: // up
					break;
				case 44: // down
					break;
				case 67: // del
					cont = 0;
					c = 255;
					break;
				default:
					break;
			}
		} while (c != 255);
	} while (cont == 1);
	return 0;
}
